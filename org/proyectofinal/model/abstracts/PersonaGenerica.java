package org.proyectofinal.model.abstracts;

/**
 * Clase abstracta que representa los datos basicos de una PersonaGenerica.
 * 
 * @author Ivan Guerra
 * @version 1.0.0
 */
public abstract class PersonaGenerica {
	
	/** El dni de la PersonaGenerica. */
	protected String dni = null;
	
	/** El nombre de la PersonaGenerica. */
	protected String nombre = null;
	
	/** El apellido de la PersonaGenerica. */
	protected String apellido = null;

}