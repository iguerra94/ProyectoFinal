package org.proyectofinal.model.abstracts;

/**
 * Clase abstracta que representa los datos basicos de una <code>PersonaGenerica</code>.
 * 
 * @author Ivan Guerra
 * @version 1.0.0
 */
public abstract class PersonaGenerica {
	
	/** El dni de la <code>PersonaGenerica</code>. */
	protected String dni = null;
	
	/** El nombre de la <code>PersonaGenerica</code>. */
	protected String nombre = null;
	
	/** El apellido de la <code>PersonaGenerica</code>. */
	protected String apellido = null;

}